class fordemo {
    public static void main (String args[]) {

        int c; //переменная цикла

        //в цикле выполняется 5 итераций
        for (c = 0; c < 5; c ++)
            System.out.println("c = " + c);
        // начиная с c = 0, пока c < 5, с увеличивать на 1,  и выводить каждый раз с = ...
        System.out.println("Выполнено!");
    }
}
